# We are creating variables
tabby_cat = "\tI'm tabbed in."
# \n would make the text "on a line." go to the next line
persian_cat = "I'm split\non a line."
backslash_cat = "I'm \\ a \\ cat."

# This is another variable that im going to create
# The triple quote will allow us to type as many lines as we want
# The \t is a TAB so it will space it out the print "Cat food"
# If you add a comment in the triple quote then it would show the comment and if you use a ''' single quote it makes no difference than a """ triple quote
fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
"""
# This will print out our variables
print(tabby_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)